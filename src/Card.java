import java.util.ArrayList;

public class Card {
    String name;
    int hp;
    ArrayList<String> moves;
    String type;
    String rarity;
    int retreatCost;
    String weaknesses;

    public Card(int pokedexNumber) {

        this.name = name;
        this.hp = hp;
        this.moves = moves;
        this.type = type;
        this.rarity = rarity;
        this.retreatCost = retreatCost;
        this.weaknesses = weaknesses;

    }


}
